const buildSentences = (language: Language, source: Record<Category, string[]>): Sentence[] =>
  categories.flatMap((category) =>
    source[category].map((text, index) => ({
      id: `${language}-${category.toLowerCase().replace(/\s/g, '-')}-${index + 1}`,
      language,
      category,
      text,
    })),
  );

export const sentenceBank: Sentence[] = [
  ...buildSentences('en', englishByCategory),
  ...buildSentences('es', spanishByCategory),
];

export const allCategories = categories;

export const getSentences = (language: Language, category?: Category) =>
  sentenceBank.filter((sentence) => sentence.language === language && (!category || sentence.category === category));

export const getSentenceById = (id: string) => sentenceBank.find((sentence) => sentence.id === id);
